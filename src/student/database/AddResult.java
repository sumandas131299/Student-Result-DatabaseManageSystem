    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollNo = jTextField1.getText();
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("SELECT * FROM student WHERE rollNO='"+rollNo+"'");
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            if(!rs.first()){
                JOptionPane.showMessageDialog(null,"Entered rollNo is Wrong");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String rollNo= jTextField1.getText();
        int physics = Integer.parseInt(jTextField2.getText());
        int math = Integer.parseInt(jTextField3.getText());
        int chemistry = Integer.parseInt(jTextField4.getText());
        int electrical = Integer.parseInt(jTextField5.getText());
        int architecture = Integer.parseInt(jTextField6.getText());
        int result =0;
        result = physics+math+chemistry+electrical+architecture;
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            st.execute("INSERT INTO result VALUES ('"+rollNo+"','"+physics+"','"+math+"','"+chemistry+"','"+electrical+"','"+architecture+"','"+result+"')");
            JOptionPane.showMessageDialog(null, "Sucessfully added");
            setVisible(false);
            AddResult frame = new AddResult();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
            
           
            
        }
        catch(Exception e ){
           JOptionPane.showMessageDialog(null, e.toString()); 
        }
    }//GEN-LAST:event_jButton2ActionPerformed


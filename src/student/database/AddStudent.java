    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                // TODO add your handling code here:
        String name = jTextField3.getText();
        String rollNo = jTextField2.getText();
        String gender = (String)jComboBox1.getSelectedItem();
        String fatherName = jTextField4.getText();
        String course =(String)jComboBox3.getSelectedItem();
        String section =(String)jComboBox5.getSelectedItem();
        
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            st.executeUpdate("INSERT INTO student VALUES('"+name+"','"+rollNo+"','"+gender+"','"+fatherName+"','"+course+"','"+section+"')");
            JOptionPane.showMessageDialog(null,"Sucessfully Added");
            setVisible(false);
            AddStudent frame = new AddStudent();
            frame.setVisible(true);
            frame.setLocation(null);
                  
        }
        catch(Exception e){
             JOptionPane.showMessageDialog(null,e.toString());
    }
                
    }//GEN-LAST:event_jButton1ActionPerformed

